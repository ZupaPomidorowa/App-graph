package com.example.appgraph;

public class NodeXY {
    int x;
    int y;
    int i;
    int j;
    int nodeNumber;

    public NodeXY(int nodeNumber, int x, int y, int i, int j) {
        this.nodeNumber = nodeNumber;
        this.x = x;
        this.y = y;
        this.i = i;
        this.j = j;
    }
}
